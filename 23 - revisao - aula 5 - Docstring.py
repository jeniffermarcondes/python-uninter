def soma(x = 0, y = 0, z = 0):
    """

    Retorna o somatório de até 3 valores numéricos quaisquer.
    Todos os parâmetros são opcionais.

    x: valor númerico (opcional)
    y: valor númerico (opcional)
    z: valor númerico (opcional)
    return: soma inteira
    """

    return x+y+z

